public class FloadTriangle {
public static void main(String[] args) {
	int sum=1;
	int n=5;
	for(int i=1;i<=n;++i){ 
		  for(int j=1;j<=i;++j){ 
			  if(sum<=n) {
		    System.out.print(sum+" ");
		   sum++;
		    } 
		   // //new line 
		  } System.out.println();
		  } 
}} 